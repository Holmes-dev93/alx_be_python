# Objective: Calculate simple interest (I = P * R * T).
# Directory: python_introduction/
# File: simple_interest.py

# 1. Define the required variables: Principal (P), Rate (R), and Time (T)
principal = 1000  # $1000 initial investment
rate = 0.05       # 5% annual interest rate (as a decimal)
time = 3          # 3 years investment time

# 2. Calculate the simple interest using the formula: I = P * R * T
interest = principal * rate * time

# 3. Print the calculated interest in the required format
print(f"The simple interest is: {interest}")
